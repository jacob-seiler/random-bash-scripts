#!/bin/bash

# 1. a name
# 2. a place
# 3. a four-letter subject abbreviation such as CISC or MATH
# 4. a course number
# 5. a letter grade

name = $1
place = $2
course = $3
cnumb = $4
grade = $5

echo "Once upon a time, there was a student named $name who came from $place. Their favorite course was $course $cnumb and their grade in that course was $grade."